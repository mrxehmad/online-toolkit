// Helper function to get metadata for a given path
export const getMetadataForPath = (path) => {
  // Normalize path (remove trailing slash, ensure it starts with /)
  const normalizedPath = path === '/' ? '/' : path.replace(/\/$/, '');
  return toolMetadata[normalizedPath] || null;
};

// Helper function to generate keywords from tool name and description
export const generateKeywords = (name, description, additionalKeywords = []) => {
  const nameWords = name.toLowerCase().split(/\s+/);
  const descWords = description.toLowerCase().split(/\s+/).filter(word => word.length > 3);
  const combined = [...nameWords, ...descWords, ...additionalKeywords];
  return [...new Set(combined)].slice(0, 10); // Remove duplicates and limit to 10
};

